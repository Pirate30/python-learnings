# importing math
import math

# ceil
print(math.ceil(10.5))  # 11

# ------------------------------

# floor
print(math.floor(10.5))  # 10

# ------------------------------

# fabs - gives absolute value
print(math.fabs(-5))  # 5.0


# -------------------------------

# factorial
print(math.factorial(5))  # 120

# ---------------------------------

# fsum - returns sum of all the elements from iterable datatype
x = [1, 3, 5, 7, 9]
print(math.fsum(x))  # 25.0

# ---------------------------------

# sqrt
print(math.sqrt(256))  # 16.0

# ------------------------------------
